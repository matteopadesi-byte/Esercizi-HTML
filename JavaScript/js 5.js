num1 = prompt("Scegli un numero:");
num2 = prompt("Scegli un altro numero:");
ope = prompt("Scegli l'operazione da eseguire:");

if (ope === "+") {
  let somma = num1 + num2;
  console.log(`${num1} + ${num2} = ${somma}`);
} else if (ope === "-") {
  let sottrazione = num1 - num2;
  console.log(`${num1} - ${num2} = ${sottrazione}`);
} else if (ope === "*") {
  let moltiplicazione = num1 * num2;
  console.log(`${num1} * ${num2} = ${moltiplicazione}`);
} else if (ope === "*") {
  let moltiplicazione = num1 * num2;
  console.log(`${num1} * ${num2} = ${moltiplicazione}`);
}
